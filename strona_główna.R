## I Konferencja Psychologiczna Gdańskiego Uniwersytetu Medycznego

#### SKN Psychologii Gdańskiego Uniwersytetu Medycznego ma przyjemność zaprosić Państwa I Konferencję Psychologiczną Gdańskiego Uniwersytetu Medycznego.

#### Konferencja to wydarzenie adresowane do studentów psychologii, psychologii zdrowia oraz wszystkich innych zainteresowanych tematyką.

